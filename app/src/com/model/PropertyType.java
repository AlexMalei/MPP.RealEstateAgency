package com.model;

public class PropertyType {
    private int id;
    private String name;

    void setId(int id) {
        this.id = id;
    }

    void setName(String name) {
        this.name = name;
    }

    int getId() {
        return this.id;
    }

    String getName() {
        return this.name;
    }
}
